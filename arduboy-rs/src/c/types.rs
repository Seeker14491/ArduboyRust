// https://gcc.gnu.org/wiki/avr-gcc#Type_Layout

#[allow(non_camel_case_types)]
pub type c_char = i8;

#[allow(non_camel_case_types)]
pub type c_uchar = u8;

#[allow(non_camel_case_types)]
pub type c_size_t = u16;

#[allow(non_camel_case_types)]
pub type c_int = i16;

#[allow(non_camel_case_types)]
pub type c_uint = u16;

#[allow(non_camel_case_types)]
pub type c_long = i32;

#[allow(non_camel_case_types)]
pub type c_ulong = u32;

#[allow(non_camel_case_types)]
pub type c_longlong = i64;

#[allow(non_camel_case_types)]
pub type c_ulonglong = u64;

#[allow(non_camel_case_types)]
pub type c_float = f32;

#[allow(non_camel_case_types)]
pub type c_double = f32;
